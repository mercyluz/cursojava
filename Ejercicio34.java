public class Ejercicio34 {
    public static void main(String[] args) {
        // Valores máximo y mínimo de int
        int intMax = Integer.MAX_VALUE;
        int intMin = Integer.MIN_VALUE;
        System.out.println("int: Máximo = " + intMax + ", Mínimo = " + intMin);

        // Valores máximo y mínimo de double
        double doubleMax = Double.MAX_VALUE;
        double doubleMin = Double.MIN_VALUE;
        System.out.println("double: Máximo = " + doubleMax + ", Mínimo = " + doubleMin);

        // Valores máximo y mínimo de char
        char charMax = Character.MAX_VALUE;
        char charMin = Character.MIN_VALUE;
        System.out.println("char: Máximo = " + (int) charMax + ", Mínimo = " + (int) charMin);
    }
}
